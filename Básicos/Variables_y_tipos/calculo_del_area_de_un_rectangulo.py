# Solicita al usuario que ingrese la lonitud y la anchura de un rectángulo y calcula su área (base * altura)

input("Ingresa la base del rectángulo: ")
input("Ingresa la altura del rectángulo: ")

base = input
altura = input
area = base * altura

print(f"El area del rectángulo es base ({base}) * altura ({altura}), cuyo resultado es: {area}")